package day24_loops;

public class SwitchInLoop {
    public static void main(String[] args) {
        for (int i=0; i<=5; i++){

            switch (i){
                case 0:
                System.out.println("zero");
                break;
                case 1:
                    System.out.println("One");
                    break;
                case 2:
                    System.out.println("Two");
                    break;
                case 3:
                    System.out.println("Three");
                    break;
            }



        }
    }
}
