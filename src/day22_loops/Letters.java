package day22_loops;

public class Letters {
    public static void main(String[] args) {
        // a-z lowercase

        char lowerCaseUp = 'a';

        while (lowerCaseUp <= 'z'){

            System.out.print(lowerCaseUp + " ");
            lowerCaseUp++;
        }

        System.out.println();
        // Z = A uppercase

        char upperCase = 'Z';

        while (upperCase >= 'A'){
            System.out.print(upperCase + " ");
            upperCase--;
        }












    }
}
